def sql_injection_test(url, param):
    payload = {param: "' OR '1'='1"}
    try:
        response = requests.get(url, params=payload)
        if "syntax error" in response.text.lower() or "mysql" in response.text.lower() or "you have an error in your sql syntax" in response.text.lower():
            print(f"{Fore.RED}[!] Le site est vulnérable à l'injection SQL!{Style.RESET_ALL}")
        else:
            print(f"{Fore.GREEN}[+] Le site ne semble pas être vulnérable à l'injection SQL.{Style.RESET_ALL}")
    except Exception as e:
        print(f"{Fore.RED}[!] Erreur lors de la tentative d'injection SQL : {e}{Style.RESET_ALL}")
